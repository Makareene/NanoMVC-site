<h1>Examples</h1>

<p>
The best way to learn NanoMVC is by exploring real projects.
The following public repositories demonstrate different approaches to building applications with NanoMVC and can be used as practical references.
</p>

<h2>NanoMVC Blog</h2>

<p>
A personal blog built with NanoMVC.
The project demonstrates how to create a content-driven website without a database by using controllers and methods as articles through the BlogMenu library.
</p>

<p>
GitHub Repository:
<a href="https://github.com/&#8203;Makareene/&#8203;NanoMVC-blog" target="_blank" rel="noopener noreferrer">https://github.com/&#8203;Makareene/&#8203;NanoMVC-blog</a>
</p>

<h2>NanoMVC Site</h2>

<p>
The official NanoMVC documentation website.
This project demonstrates documentation generation, article navigation, metadata-driven content, and practical usage of NanoMVC libraries and views.
</p>

<p>
GitHub Repository:
<a href="https://github.com/&#8203;Makareene/&#8203;NanoMVC-site" target="_blank" rel="noopener noreferrer">https://github.com/&#8203;Makareene/&#8203;NanoMVC-site</a>
</p>

<h2>Why Explore These Projects?</h2>

<ul>
  <li>See how NanoMVC applications are organized in practice</li>
  <li>Learn how controllers, views, libraries, and BlogMenu work together</li>
  <li>Reuse ideas and code patterns in your own projects</li>
  <li>Discover real-world solutions built with the framework</li>
</ul>

<p>
Both repositories are open source and can be freely explored, modified, and used as learning resources when building your own NanoMVC applications.
</p>
